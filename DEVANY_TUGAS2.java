public static double faktorial(int angka){
    if(angka==1 || angka==0){
        return 1;
    }else{
        return angka*faktorial(angka-1);
    }
}
public static double deret(double x, double a, int suku) {
        double hasil = 1;
        for(int p=1; p<=suku; p++){
            hasil *=(x-a)/(1-a); 
        }
        return hasil;
}
