public static double PerhitunganSoal4(double a, double n, double x) {
        double[] f = new double[10001];
        f[0] = Math.log(-a);
        double Fungsi=0;
        double es = 0.5 * Math.pow(10, 2-n);
        for(int p=1; p<=10000; p++){
            f[p]=f[p-1] + (Math.pow(-1,p+1)*Math.pow(x-a, p))/(p*Math.pow(a,p));
            double ea = ((f[p]-f[p-1])/f[p])*100;
            if(Math.abs(ea)<es){
                Fungsi += f[p];
                System.out.println(p);
                break;
            }
        }
        double Output = Math.floor(Fungsi*10000)/10000;
        return Output;
    }
public static double PerhitunganSoal5(double a, double n, double x) {
        double[] f = new double[10001];
        f[0] = a/(1-a);
        double Fungsi=0;
        double es = 0.5 * Math.pow(10, 2-n);
        for(int p=1; p<=10000; p++){
            f[p]=f[p-1]+deret(x, a, p);
            double ea = ((f[p]-f[p-1])/f[p])*100;
            if(Math.abs(ea)<es){
                Fungsi += f[p];
                System.out.println(p);
                break;
            }
            }
        double Output = Math.floor(Fungsi*10000)/10000;
        return Output;
}

public static double PerhitunganSoal7(double a, double n, double x) {
        double[] f = new double[10001];
        f[0] = a*Math.exp(2*a);
        double es = 0.5 * Math.pow(10, 2-n);
        double Fungsi=0;
        double k;
        double l = 1;
        for(int p=1; p<=10000; p++){
            k = Math.pow(2,p);
            f[p] = f[p-1] = (k*a*Math.exp(2*x)+l*Math.exp(2*a))*Math.pow(x-a,p)/faktorial(p);
            l = 2*l+k;
            double ea = ((f[p]-f[p-1])/f[p])*100;
            if(Math.abs(ea)<es){
                Fungsi += f[p];
            }
        }
        double Output = Math.floor(Fungsi*10000)/10000;
        return Output;
}


    private void hitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitungActionPerformed
        // TODO add your handling code here:
        double a = Double.parseDouble(inputa.getText());
        double n = Double.parseDouble(inputn.getText());
        double x = Double.parseDouble(inputx.getText());
        switch(soal.getSelectedIndex()){
            case 1:
                if(a<0 && x<0){
                    inputh.setText(String.valueOf(PerhitunganSoal4(a,n,x)));
                }else{
                    JOptionPane.showMessageDialog(null, "Input yang dimasukkan salah\n"+"Nilai a dan x harus kurang dari 0");
                }
                break;
            case 2:
                if(a!=1 && (x>-2 && x<2)){
                    inputh.setText(String.valueOf(PerhitunganSoal5(a,n,x)));
                }else{
                    JOptionPane.showMessageDialog(null, "Input yang dimasukkan salah\n"+"Nilai x harus lebih dari -2 dan kurang dari 2");
                }
                break;
            case 3:
                inputh.setText(String.valueOf(PerhitunganSoal7(a,n,x)));
                break;
            default:
                JOptionPane.showMessageDialog(null, "Masukkan Soal");
    }//GEN-LAST:event_hitungActionPerformed
    }
